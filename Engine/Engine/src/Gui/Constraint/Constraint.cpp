#include "Gui/Constraint/Constraint.h"
#include "Gui/Container.h"

//////////////////////////////////////////////////////////////////////////

GuiLayout::GuiLayout(GuiContainer* container)
    : owner(container)
{

}

//////////////////////////////////////////////////////////////////////////

const f32 GuiLayout::GetX() const
{
    return xConstraint->Solve(owner->GetLeft());
}

//////////////////////////////////////////////////////////////////////////

const f32 GuiLayout::GetY() const
{
    return yConstraint->Solve(owner->GetTop());
}

//////////////////////////////////////////////////////////////////////////

const f32 GuiLayout::GetWidth() const
{
    return widthConstraint->Solve(owner->GetWidth());
}

//////////////////////////////////////////////////////////////////////////

const f32 GuiLayout::GetHeight() const
{
    return heightConstraint->Solve(owner->GetHeight());
}

