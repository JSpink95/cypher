#include "Core/Utility/TimeUtils.h"

//////////////////////////////////////////////////////////////////////////

f32 Time::GetDeltaTime()
{
    Ref<Time> time = GetTime();
    if (time)
    {
        return time->GetDeltaTimeImpl();
    }

    return 0.0f;
}

//////////////////////////////////////////////////////////////////////////

f32 Time::GetAppTime()
{
    Ref<Time> time = GetTime();
    if (time)
    {
        return time->GetAppTimeImpl();
    }

    return 0.0f;
}

//////////////////////////////////////////////////////////////////////////

void Time::UpdateTime(const f32 deltaTime)
{
    Ref<Time> time = GetTime();
    if (time)
    {
        time->UpdateTimeImpl(deltaTime);
    }
}

//////////////////////////////////////////////////////////////////////////

void Time::UpdateTimeImpl(const f32 dt)
{
    deltaTime = dt;
    appTime += dt;
}

