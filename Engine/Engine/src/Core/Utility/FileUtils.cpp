#include <fstream>

//////////////////////////////////////////////////////////////////////////

namespace File
{
	std::optional<std::string> LoadAsString(const std::string& filepath)
	{
		std::ifstream file(filepath);
		if (file)
		{
			std::string result, line;
			while (std::getline(file, line)) result += (line + "\n");
			file.close();

			return result;
		}

		return {};
	}
}

