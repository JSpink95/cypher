namespace TextureParameterType
{
	enum Enum
	{
		FilterMode,
		WrapMode,
		Max
	};
}

namespace TextureParameterValue
{
	enum Enum
	{
		Linear,
		Nearest,
		Repeat,
		ClampToEdge,
		Max
	};
}

//////////////////////////////////////////////////////////////////////////

class Texture2D: public IDisposable
{
public:
    virtual ~Texture2D() = default;

	virtual void Bind(const u32 location = 0) = 0;
	virtual void Unbind() = 0;

	virtual void SetTextureParameter(const TextureParameterType::Enum type, const TextureParameterValue::Enum value) = 0;
};

